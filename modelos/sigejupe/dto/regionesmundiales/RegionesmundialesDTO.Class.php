class RegionesmundialesDTO {
    private $cveRegionMundial;
    private $desRegionMundial;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getCveRegionMundial(){
        return $this->cveRegionMundial;
    }
    public function setCveRegionMundial($cveRegionMundial){
        $this->cveRegionMundial=$cveRegionMundial;
    }
    public function getDesRegionMundial(){
        return $this->desRegionMundial;
    }
    public function setDesRegionMundial($desRegionMundial){
        $this->desRegionMundial=$desRegionMundial;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
    public function toString(){
        return array("cveRegionMundial"=>$this->cveRegionMundial,
"desRegionMundial"=>$this->desRegionMundial,
"activo"=>$this->activo,
"fechaRegistro"=>$this->fechaRegistro,
"fechaActualizacion"=>$this->fechaActualizacion);
    }
}
?>