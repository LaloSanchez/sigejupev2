class AudienciasjuzgadorDTO {
    private $idAudienciaJuez;
    private $idAudiencia;
    private $idJuzgador;
    private $cveFuncionJuzgador;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getIdAudienciaJuez(){
        return $this->idAudienciaJuez;
    }
    public function setIdAudienciaJuez($idAudienciaJuez){
        $this->idAudienciaJuez=$idAudienciaJuez;
    }
    public function getIdAudiencia(){
        return $this->idAudiencia;
    }
    public function setIdAudiencia($idAudiencia){
        $this->idAudiencia=$idAudiencia;
    }
    public function getIdJuzgador(){
        return $this->idJuzgador;
    }
    public function setIdJuzgador($idJuzgador){
        $this->idJuzgador=$idJuzgador;
    }
    public function getCveFuncionJuzgador(){
        return $this->cveFuncionJuzgador;
    }
    public function setCveFuncionJuzgador($cveFuncionJuzgador){
        $this->cveFuncionJuzgador=$cveFuncionJuzgador;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
