class CertificacionesDTO {
    private $idCertificacion;
    private $idActuacion;
    private $numEmpleadoAutCertificacion;
    private $lugarCertificacion;
    private $horaCertificacion;
    public function getIdCertificacion(){
        return $this->idCertificacion;
    }
    public function setIdCertificacion($idCertificacion){
        $this->idCertificacion=$idCertificacion;
    }
    public function getIdActuacion(){
        return $this->idActuacion;
    }
    public function setIdActuacion($idActuacion){
        $this->idActuacion=$idActuacion;
    }
    public function getNumEmpleadoAutCertificacion(){
        return $this->numEmpleadoAutCertificacion;
    }
    public function setNumEmpleadoAutCertificacion($numEmpleadoAutCertificacion){
        $this->numEmpleadoAutCertificacion=$numEmpleadoAutCertificacion;
    }
    public function getLugarCertificacion(){
        return $this->lugarCertificacion;
    }
    public function setLugarCertificacion($lugarCertificacion){
        $this->lugarCertificacion=$lugarCertificacion;
    }
    public function getHoraCertificacion(){
        return $this->horaCertificacion;
    }
    public function setHoraCertificacion($horaCertificacion){
        $this->horaCertificacion=$horaCertificacion;
    }
    public function toString(){
        return array("idCertificacion"=>$this->idCertificacion,
"idActuacion"=>$this->idActuacion,
"numEmpleadoAutCertificacion"=>$this->numEmpleadoAutCertificacion,
"lugarCertificacion"=>$this->lugarCertificacion,
"horaCertificacion"=>$this->horaCertificacion);
    }
}
?>