class MedidascautelaresDTO {
    private $idMedidaCautelar;
    private $idActuacion;
    private $idImputadoCarpeta;
    private $apelada;
    private $activo;
    private $fechaInicio;
    private $fechaTermina;
    private $cveTipoMedidaCautelar;
    private $cveAutoridadEmisora;
    public function getIdMedidaCautelar(){
        return $this->idMedidaCautelar;
    }
    public function setIdMedidaCautelar($idMedidaCautelar){
        $this->idMedidaCautelar=$idMedidaCautelar;
    }
    public function getIdActuacion(){
        return $this->idActuacion;
    }
    public function setIdActuacion($idActuacion){
        $this->idActuacion=$idActuacion;
    }
    public function getIdImputadoCarpeta(){
        return $this->idImputadoCarpeta;
    }
    public function setIdImputadoCarpeta($idImputadoCarpeta){
        $this->idImputadoCarpeta=$idImputadoCarpeta;
    }
    public function getApelada(){
        return $this->apelada;
    }
    public function setApelada($apelada){
        $this->apelada=$apelada;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaInicio(){
        return $this->fechaInicio;
    }
    public function setFechaInicio($fechaInicio){
        $this->fechaInicio=$fechaInicio;
    }
    public function getFechaTermina(){
        return $this->fechaTermina;
    }
    public function setFechaTermina($fechaTermina){
        $this->fechaTermina=$fechaTermina;
    }
    public function getCveTipoMedidaCautelar(){
        return $this->cveTipoMedidaCautelar;
    }
    public function setCveTipoMedidaCautelar($cveTipoMedidaCautelar){
        $this->cveTipoMedidaCautelar=$cveTipoMedidaCautelar;
    }
    public function getCveAutoridadEmisora(){
        return $this->cveAutoridadEmisora;
    }
    public function setCveAutoridadEmisora($cveAutoridadEmisora){
        $this->cveAutoridadEmisora=$cveAutoridadEmisora;
    }
    public function toString(){
        return array("idMedidaCautelar"=>$this->idMedidaCautelar,
"idActuacion"=>$this->idActuacion,
"idImputadoCarpeta"=>$this->idImputadoCarpeta,
"apelada"=>$this->apelada,
"activo"=>$this->activo,
"fechaInicio"=>$this->fechaInicio,
"fechaTermina"=>$this->fechaTermina,
"cveTipoMedidaCautelar"=>$this->cveTipoMedidaCautelar,
"cveAutoridadEmisora"=>$this->cveAutoridadEmisora);
    }
}
