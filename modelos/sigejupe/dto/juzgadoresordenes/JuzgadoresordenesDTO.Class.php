class JuzgadoresordenesDTO {
    private $idJuzgadorOrden;
    private $idSolicitudOrden;
    private $idJuzgador;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getIdJuzgadorOrden(){
        return $this->idJuzgadorOrden;
    }
    public function setIdJuzgadorOrden($idJuzgadorOrden){
        $this->idJuzgadorOrden=$idJuzgadorOrden;
    }
    public function getIdSolicitudOrden(){
        return $this->idSolicitudOrden;
    }
    public function setIdSolicitudOrden($idSolicitudOrden){
        $this->idSolicitudOrden=$idSolicitudOrden;
    }
    public function getIdJuzgador(){
        return $this->idJuzgador;
    }
    public function setIdJuzgador($idJuzgador){
        $this->idJuzgador=$idJuzgador;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
