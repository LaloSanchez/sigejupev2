class ChatCerradosDTO {

    private $idChatCerrado;
    private $chatId;
    private $fechaCierre;

    function getIdChatCerrado() {
        return $this->idChatCerrado;
    }

    function getChatId() {
        return $this->chatId;
    }

    function getFechaCierre() {
        return $this->fechaCierre;
    }

    function setIdChatCerrado($idChatCerrado) {
        $this->idChatCerrado = $idChatCerrado;
    }

    function setChatId($chatId) {
        $this->chatId = $chatId;
    }

    function setFechaCierre($fechaCierre) {
        $this->fechaCierre = $fechaCierre;
    }

    public function toString() {
        return array("idChatCerrado" => $this->idChatCerrado,
            "chatId" => $this->chatId,
            "fechaCierre" => $this->fechaCierre);
    }

}

