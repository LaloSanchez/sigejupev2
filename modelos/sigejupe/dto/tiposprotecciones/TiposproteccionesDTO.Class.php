class TiposproteccionesDTO {
    private $cveTipoProteccion;
    private $desTipoProteccion;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getCveTipoProteccion(){
        return $this->cveTipoProteccion;
    }
    public function setCveTipoProteccion($cveTipoProteccion){
        $this->cveTipoProteccion=$cveTipoProteccion;
    }
    public function getDesTipoProteccion(){
        return $this->desTipoProteccion;
    }
    public function setDesTipoProteccion($desTipoProteccion){
        $this->desTipoProteccion=$desTipoProteccion;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
    public function toString(){
        return array("cveTipoProteccion"=>$this->cveTipoProteccion,
"desTipoProteccion"=>$this->desTipoProteccion,
"activo"=>$this->activo,
"fechaRegistro"=>$this->fechaRegistro,
"fechaActualizacion"=>$this->fechaActualizacion);
    }
}
