class TerminosDTO {
    private $idTermino;
    private $texto;
    private $activo;
    private $cveTipoTermino;
    public function getIdTermino(){
        return $this->idTermino;
    }
    public function setIdTermino($idTermino){
        $this->idTermino=$idTermino;
    }
    public function getTexto(){
        return $this->texto;
    }
    public function setTexto($texto){
        $this->texto=$texto;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getCveTipoTermino(){
        return $this->cveTipoTermino;
    }
    public function setCveTipoTermino($cveTipoTermino){
        $this->cveTipoTermino=$cveTipoTermino;
    }
    public function toString(){
        return array("idTermino"=>$this->idTermino,
"texto"=>$this->texto,
"activo"=>$this->activo,
"cveTipoTermino"=>$this->cveTipoTermino);
    }
}
?>