class MediosnotificacionesDTO {

    private $cveMedioNotificacion;
    private $desMedioNotificacion;
    private $archivoController;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;

    public function getCveMedioNotificacion() {
        return $this->cveMedioNotificacion;
    }

    public function setCveMedioNotificacion($cveMedioNotificacion) {
        $this->cveMedioNotificacion = $cveMedioNotificacion;
    }

    public function getDesMedioNotificacion() {
        return $this->desMedioNotificacion;
    }

    public function setDesMedioNotificacion($desMedioNotificacion) {
        $this->desMedioNotificacion = $desMedioNotificacion;
    }

    public function getActivo() {
        return $this->activo;
    }

    public function setActivo($activo) {
        $this->activo = $activo;
    }

    public function getFechaRegistro() {
        return $this->fechaRegistro;
    }

    public function setFechaRegistro($fechaRegistro) {
        $this->fechaRegistro = $fechaRegistro;
    }

    public function getFechaActualizacion() {
        return $this->fechaActualizacion;
    }

    public function setFechaActualizacion($fechaActualizacion) {
        $this->fechaActualizacion = $fechaActualizacion;
    }

    function getArchivoController() {
        return $this->archivoController;
    }

    function setArchivoController($archivoController) {
        $this->archivoController = $archivoController;
        return $this;
    }

    public function toString() {
        return array(
            "cveMedioNotificacion" => $this->cveMedioNotificacion,
            "desMedioNotificacion" => $this->desMedioNotificacion,
            "archivoController" => $this->archivoController,
            "activo" => $this->activo,
            "fechaRegistro" => $this->fechaRegistro,
            "fechaActualizacion" => $this->fechaActualizacion);
    }

}

