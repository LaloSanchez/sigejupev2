class ViolenciadegenerocarpetasDTO {
    private $idViolenciaDeGeneroCarpeta;
    private $cveEfecto;
    private $idImpOfeDelCarpeta;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getIdViolenciaDeGeneroCarpeta(){
        return $this->idViolenciaDeGeneroCarpeta;
    }
    public function setIdViolenciaDeGeneroCarpeta($idViolenciaDeGeneroCarpeta){
        $this->idViolenciaDeGeneroCarpeta=$idViolenciaDeGeneroCarpeta;
    }
    public function getCveEfecto(){
        return $this->cveEfecto;
    }
    public function setCveEfecto($cveEfecto){
        $this->cveEfecto=$cveEfecto;
    }
    public function getIdImpOfeDelCarpeta(){
        return $this->idImpOfeDelCarpeta;
    }
    public function setIdImpOfeDelCarpeta($idImpOfeDelCarpeta){
        $this->idImpOfeDelCarpeta=$idImpOfeDelCarpeta;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
