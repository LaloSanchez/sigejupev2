class DelitoselectronicoDTO {
    private $idDelitoElectronico;
    private $idMateriaLiti;
    private $cveDelito;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getIdDelitoElectronico(){
        return $this->idDelitoElectronico;
    }
    public function setIdDelitoElectronico($idDelitoElectronico){
        $this->idDelitoElectronico=$idDelitoElectronico;
    }
    public function getIdMateriaLiti(){
        return $this->idMateriaLiti;
    }
    public function setIdMateriaLiti($idMateriaLiti){
        $this->idMateriaLiti=$idMateriaLiti;
    }
    public function getCveDelito(){
        return $this->cveDelito;
    }
    public function setCveDelito($cveDelito){
        $this->cveDelito=$cveDelito;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
    public function toString(){
        return array("idDelitoElectronico"=>$this->idDelitoElectronico,
"idMateriaLiti"=>$this->idMateriaLiti,
"cveDelito"=>$this->cveDelito,
"activo"=>$this->activo,
"fechaRegistro"=>$this->fechaRegistro,
"fechaActualizacion"=>$this->fechaActualizacion);
    }
}
