class TiposdefensoresDTO {
    private $cveTipoDefensor;
    private $desTipoDefensor;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getCveTipoDefensor(){
        return $this->cveTipoDefensor;
    }
    public function setCveTipoDefensor($cveTipoDefensor){
        $this->cveTipoDefensor=$cveTipoDefensor;
    }
    public function getDesTipoDefensor(){
        return $this->desTipoDefensor;
    }
    public function setDesTipoDefensor($desTipoDefensor){
        $this->desTipoDefensor=$desTipoDefensor;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
    public function toString(){
        return array("cveTipoDefensor"=>$this->cveTipoDefensor,
"desTipoDefensor"=>$this->desTipoDefensor,
"activo"=>$this->activo,
"fechaRegistro"=>$this->fechaRegistro,
"fechaActualizacion"=>$this->fechaActualizacion);
    }
}
?>