    private $fechaRegistro;
    private $fechaActualizacion;
    public function getIdAntecedeCarpeta(){
        return $this->idAntecedeCarpeta;
    }
    public function setIdAntecedeCarpeta($idAntecedeCarpeta){
        $this->idAntecedeCarpeta=$idAntecedeCarpeta;
    }
    public function getIdCarpetaPadre(){
        return $this->idCarpetaPadre;
    }
    public function setIdCarpetaPadre($idCarpetaPadre){
        $this->idCarpetaPadre=$idCarpetaPadre;
    }
    public function getIdCarpetaHija(){
        return $this->idCarpetaHija;
    }
    public function setIdCarpetaHija($idCarpetaHija){
        $this->idCarpetaHija=$idCarpetaHija;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
