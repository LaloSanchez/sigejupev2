class ReasignaciontocasDTO {
    private $idReasignacionToca;
    private $idReferenciaOrigen;
    private $idReferenciaDestino;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getIdReasignacionToca(){
        return $this->idReasignacionToca;
    }
    public function setIdReasignacionToca($idReasignacionToca){
        $this->idReasignacionToca=$idReasignacionToca;
    }
    public function getIdReferenciaOrigen(){
        return $this->idReferenciaOrigen;
    }
    public function setIdReferenciaOrigen($idReferenciaOrigen){
        $this->idReferenciaOrigen=$idReferenciaOrigen;
    }
    public function getIdReferenciaDestino(){
        return $this->idReferenciaDestino;
    }
    public function setIdReferenciaDestino($idReferenciaDestino){
        $this->idReferenciaDestino=$idReferenciaDestino;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
    public function toString(){
        return array("idReasignacionToca"=>$this->idReasignacionToca,
"idReferenciaOrigen"=>$this->idReferenciaOrigen,
"idReferenciaDestino"=>$this->idReferenciaDestino,
"activo"=>$this->activo,
"fechaRegistro"=>$this->fechaRegistro,
"fechaActualizacion"=>$this->fechaActualizacion);
    }
}
