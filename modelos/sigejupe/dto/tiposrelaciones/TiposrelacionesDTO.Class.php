class TiposrelacionesDTO {
    private $cveTipoRelacion;
    private $cveTipoCarpeta;
    private $desTipoRelacion;
    private $activo;
    private $fechaRegistro;
    private $fechaActualizacion;
    public function getCveTipoRelacion(){
        return $this->cveTipoRelacion;
    }
    public function setCveTipoRelacion($cveTipoRelacion){
        $this->cveTipoRelacion=$cveTipoRelacion;
    }
    public function getCveTipoCarpeta(){
        return $this->cveTipoCarpeta;
    }
    public function setCveTipoCarpeta($cveTipoCarpeta){
        $this->cveTipoCarpeta=$cveTipoCarpeta;
    }
    public function getDesTipoRelacion(){
        return $this->desTipoRelacion;
    }
    public function setDesTipoRelacion($desTipoRelacion){
        $this->desTipoRelacion=$desTipoRelacion;
    }
    public function getActivo(){
        return $this->activo;
    }
    public function setActivo($activo){
        $this->activo=$activo;
    }
    public function getFechaRegistro(){
        return $this->fechaRegistro;
    }
    public function setFechaRegistro($fechaRegistro){
        $this->fechaRegistro=$fechaRegistro;
    }
    public function getFechaActualizacion(){
        return $this->fechaActualizacion;
    }
    public function setFechaActualizacion($fechaActualizacion){
        $this->fechaActualizacion=$fechaActualizacion;
    }
    public function toString(){
        return array("cveTipoRelacion"=>$this->cveTipoRelacion,
"cveTipoCarpeta"=>$this->cveTipoCarpeta,
"desTipoRelacion"=>$this->desTipoRelacion,
"activo"=>$this->activo,
"fechaRegistro"=>$this->fechaRegistro,
"fechaActualizacion"=>$this->fechaActualizacion);
    }
}
