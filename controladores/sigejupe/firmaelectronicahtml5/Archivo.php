<?php

class Archivo {

    public $nombre;
    public $firma;
    public $digestion;
    public $transferencia;
    public $estado;
    public $descripcion;
    public $reporteFirmaEstado;
    public $reporteFirmaDescipcion;

    public function setReporteFirmaEstado($reporteFirmaEstado) {
        $this->reporteFirmaEstado = $reporteFirmaEstado;
    }

    public function getReporteFirmaEstado() {
        return $this->reporteFirmaEstado;
    }

    public function setReporteFirmaDescripcion($reporteFirmaDescipcion) {
        $this->reporteFirmaDescipcion = $reporteFirmaDescipcion;
    }

    public function getReporteFirmaDescripcion() {
        return $this->reporteFirmaDescipcion;
    }

    public function setEstado($estado) {
        $this->estado = $estado;
    }

    public function getEstado() {
        return $this->estado;
    }

    public function setDescripcion($descripcion) {
        $this->descripcion = $descripcion;
    }

    public function getDescripcion() {
        return $this->descripcion;
    }

    public function setNombre($nombre) {
        $this->nombre = $nombre;
    }

    public function getNombre() {
        return $this->nombre;
    }

    public function setFirma($firma) {
        $this->firma = $firma;
    }

    public function getFirma() {
        return $this->firma;
    }

    public function setDigestion($digestion) {
        $this->digestion = $digestion;
    }

    public function getDigestion() {
        return $this->digestion;
    }

    public function setTransferencia($transferencia) {
        $this->transferencia = $transferencia;
    }

    public function getTransferencia() {
        return $this->transferencia;
    }

}

?>